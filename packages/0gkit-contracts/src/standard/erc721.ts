export interface Erc721Options {
  /** NFT contract address (required — ERC-721 is per-deployment). */
  address: Address;
  network?: Network;
  rpcUrl?: string;
  signer?: Signer;
}

export function erc721(opts: Erc721Options): TypedContract<typeof Erc721Abi> {
  if (!opts.address) {
    throw new ConfigError(
      "ERC-721 requires { address } — there is no per-network singleton.",
      "Pass the deployed NFT contract address as { address: '0x...' }."
    );
  }
  return createTypedContract({
    abi: Erc721Abi,
    address: opts.address,
    network: opts.network,
    rpcUrl: opts.rpcUrl,
    signer: opts.signer,
  });
}
