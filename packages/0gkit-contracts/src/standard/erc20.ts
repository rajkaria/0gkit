export interface Erc20Options {
  /** Token contract address (required — ERC-20 is per-deployment). */
  address: Address;
  network?: Network;
  rpcUrl?: string;
  signer?: Signer;
}

export function erc20(opts: Erc20Options): TypedContract<typeof Erc20Abi> {
  if (!opts.address) {
    throw new ConfigError(
      "ERC-20 requires { address } — there is no per-network singleton.",
      "Pass the deployed token contract address as { address: '0x...' }."
    );
  }
  return createTypedContract({
    abi: Erc20Abi,
    address: opts.address,
    network: opts.network,
    rpcUrl: opts.rpcUrl,
    signer: opts.signer,
  });
}
