// Build-time registry codegen.
// Scans templates/_kits/kit.json files, validates, writes src/registry.generated.ts
// Output is idempotent and deterministic (sorted by kit name).

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

// ---------------------------------------------------------------------------
// Paths
// ---------------------------------------------------------------------------

const __dirname = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(__dirname, "..");               // packages/0gkit-kits
const repoRoot = resolve(pkgRoot, "../..");             // repo root
const kitsDir = join(repoRoot, "templates", "_kits");
const outFile = join(pkgRoot, "src", "registry.generated.ts");

// ---------------------------------------------------------------------------
// Minimal JSON-only schema validation
//
// We cannot import the TypeScript source directly from a plain .mjs script
// (no ts-node / tsx here by design).  We do a lightweight structural check
// that mirrors KitManifestSchema from src/manifest.ts:
//   - name: kebab-case string
//   - title, summary: non-empty strings
//   - domain: one of KIT_DOMAINS
//   - compatibleBases: non-empty string[]
//   - tiers: object
// ---------------------------------------------------------------------------

const KIT_DOMAINS = new Set([
  "verifiable-ai", "agent-infra", "markets", "assets", "defi",
]);

const KEBAB_RE = /^[a-z][a-z0-9-]*$/;

function validateManifest(raw, sourcePath) {
  if (typeof raw !== "object" || raw === null) {
    throw new Error(`${sourcePath}: must be a JSON object`);
  }
  if (!KEBAB_RE.test(raw.name ?? "")) {
    throw new Error(`${sourcePath}: "name" must be kebab-case, got: ${raw.name}`);
  }
  if (!raw.title || typeof raw.title !== "string") {
    throw new Error(`${sourcePath}: "title" must be a non-empty string`);
  }
  if (!KIT_DOMAINS.has(raw.domain)) {
    throw new Error(`${sourcePath}: "domain" must be one of ${[...KIT_DOMAINS].join(", ")}`);
  }
  if (!raw.summary || typeof raw.summary !== "string") {
    throw new Error(`${sourcePath}: "summary" must be a non-empty string`);
  }
  if (!Array.isArray(raw.compatibleBases) || raw.compatibleBases.length === 0) {
    throw new Error(`${sourcePath}: "compatibleBases" must be a non-empty array`);
  }
  if (typeof raw.tiers !== "object" || raw.tiers === null) {
    throw new Error(`${sourcePath}: "tiers" must be an object`);
  }
  return raw;
}

// ---------------------------------------------------------------------------
// Scan
// ---------------------------------------------------------------------------

let kitDirs;
try {
  kitDirs = readdirSync(kitsDir, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .map((e) => e.name)
    .sort();
} catch {
  // Directory doesn't exist yet — treat as empty
  kitDirs = [];
}

const manifests = [];

for (const kitName of kitDirs) {
  const manifestPath = join(kitsDir, kitName, "kit.json");
  let raw;
  try {
    raw = JSON.parse(readFileSync(manifestPath, "utf8"));
  } catch (err) {
    throw new Error(`Failed to read ${manifestPath}: ${err.message}`);
  }
  validateManifest(raw, manifestPath);
  manifests.push(raw);
}

// Sort deterministically by name (already sorted from readdirSync, but be explicit)
manifests.sort((a, b) => a.name.localeCompare(b.name));

// ---------------------------------------------------------------------------
// Codegen
// ---------------------------------------------------------------------------

const kitsJson = JSON.stringify(manifests, null, 2);

const output = `// AUTO-GENERATED by scripts/gen-registry.mjs — DO NOT EDIT BY HAND
// Regenerated on every build via the "prebuild" / "pretest" npm script.
import type { KitManifest } from "./manifest.js";

export const KITS: KitManifest[] = ${kitsJson};
`;

writeFileSync(outFile, output, "utf8");

const count = manifests.length;
console.log(
  `[gen-registry] wrote ${outFile.replace(process.cwd(), ".")} — ${count} kit${count === 1 ? "" : "s"}`,
);
